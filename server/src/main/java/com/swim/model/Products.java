package com.swim.model;

public class Products {
	private int asn;
	private int serial;

	// No Args Constructor
	public Products() {
	}

	// Getters and Setter
	public int getAsnid() {
		return asn;
	}

	public void setAsnid(int asnid) {
		this.asn = asn;
	}

	public int getSerial() {
		return serial;
	}

	public void setSerial(int serial) {
		this.serial = serial;
	}

	
}
